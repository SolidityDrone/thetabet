#!/usr/bin/env node
/**
 * Sync ponder/deployments/{network}.json into mobile contracts.generated.ts
 * and optional tunnel URL file.
 *
 * Usage: node tools/scripts/sync-deployment.mjs [polygon|anvil]
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, '../..')

const network = process.argv[2] ?? 'polygon'
const deploymentName = network === 'anvil' ? 'anvil' : 'polygon'
const deploymentFile = path.join(repoRoot, 'ponder/deployments', `${deploymentName}.json`)

if (!fs.existsSync(deploymentFile)) {
  console.error(`Deployment file not found: ${deploymentFile}`)
  process.exit(1)
}

const deployment = JSON.parse(fs.readFileSync(deploymentFile, 'utf8'))

const tunnelFile = path.join(repoRoot, 'apps/mobile/src/config/tunnel.generated.ts')
let ponderUrl = 'http://localhost:42069/graphql'
if (fs.existsSync(tunnelFile)) {
  const tunnelSrc = fs.readFileSync(tunnelFile, 'utf8')
  const match = tunnelSrc.match(/PONDER_TUNNEL_URL\s*=\s*'([^']+)'/)
  if (match?.[1]) {
    ponderUrl = match[1].endsWith('/graphql') ? match[1] : `${match[1].replace(/\/$/, '')}/graphql`
  }
}

const checksum = (address) => {
  if (!address || typeof address !== 'string') return address
  return address.startsWith('0x') ? address : `0x${address}`
}

const thetaSingleton = checksum(deployment.thetaSingleton)
const betToken = checksum(deployment.betToken)
const azuroLP = checksum(deployment.azuroLP)
const azuroCore = checksum(deployment.azuroCore)
const deployer = deployment.deployer ? checksum(deployment.deployer) : '0x0000000000000000000000000000000000000000'
const chainId = deployment.chainId ?? 137
const startBlock = deployment.startBlock ?? 0
const rpcUrl = deployment.rpcUrl ?? 'https://polygon-rpc.com'
const networkName = deployment.network ?? (network === 'anvil' ? 'polygonFork' : 'polygon')
const syncedAt = new Date().toISOString()

const contractsOut = path.join(repoRoot, 'apps/mobile/src/config/contracts.generated.ts')
const contractsTs = `// AUTO-GENERATED by tools/scripts/sync-deployment.mjs — do not edit manually.
// Source: ponder/deployments/${deploymentName}.json
// Synced: ${syncedAt}

import type { Address } from 'viem'

export const THETA_DEPLOYMENT = {
  network: '${networkName}',
  chainId: ${chainId},
  startBlock: ${startBlock},
  deployer: '${deployer}' as Address,
  thetaSingleton: '${thetaSingleton}' as Address,
  betToken: '${betToken}' as Address,
  azuroLP: '${azuroLP}' as Address,
  azuroCore: '${azuroCore}' as Address,
  rpcUrl: '${rpcUrl}',
  useMocks: ${Boolean(deployment.useMocks)},
} as const

export const PONDER_GRAPHQL_URL =
  process.env.EXPO_PUBLIC_PONDER_URL ?? '${ponderUrl}'
`

fs.writeFileSync(contractsOut, contractsTs)
console.log(`Wrote ${path.relative(repoRoot, contractsOut)}`)
console.log(`  thetaSingleton: ${thetaSingleton}`)
console.log(`  ponder:         ${ponderUrl}`)
console.log(`Ponder reads contract address from ${path.relative(repoRoot, deploymentFile)} (restart ponder after deploy)`)
